# perulangan (loop)

# for kondisi
# aksi

number_1 = [0,2,4,6,8]
print(number_1)

for i in number_1:
    print(f"i sekarang -> {i}")

print("end of program 1......\n")

# ini dengan range
number_range = range(1,10)

for i in number_range:
    print(f"i sekarang -> {i}")

print("end of program 2......\n")

# menggunakan string
string_1 = "shella is very very beautiful"

for char in string_1:
    print(char)

print("end of program 3.......\n")
